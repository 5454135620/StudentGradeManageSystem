	private void AddActionPerformed(java.awt.event.ActionEvent evt) {
		addGetP();
		if (StringUtil.isEmpty(sno)) {
			JOptionPane.showConfirmDialog(null, "学号不能为空!", "提示", JOptionPane.CLOSED_OPTION);
			return;
		}
		if (StringUtil.isEmpty(sname)) {
			JOptionPane.showConfirmDialog(null, "姓名不能为空!", "提示", JOptionPane.CLOSED_OPTION);
			return;
		}
		if (ssex == "无") {
			JOptionPane.showConfirmDialog(null, "性别不能为空!", "提示", JOptionPane.CLOSED_OPTION);
			return;
		}
		if (StringUtil.isEmpty(sage)) {
			JOptionPane.showConfirmDialog(null, "年龄不能为空|0!", "提示", JOptionPane.CLOSED_OPTION);
			return;
		}
		if (sage > 255) {
			JOptionPane.showConfirmDialog(null, "年龄不符合实际！", "提示", JOptionPane.CLOSED_OPTION);
			return;
		}
		if (StringUtil.isEmpty(sdept)) {
			JOptionPane.showConfirmDialog(null, "专业不能为空!", "提示", JOptionPane.CLOSED_OPTION);
			return;
		}
		if (StringUtil.isEmpty(sclass)) {
			JOptionPane.showConfirmDialog(null, "班级不能为空!", "提示", JOptionPane.CLOSED_OPTION);
			return;
		}
		if (StringUtil.isEmpty(stel)) {
			JOptionPane.showConfirmDialog(null, "电话号码不能为空!", "提示", JOptionPane.CLOSED_OPTION);
			return;
		}
		if (!StringUtil.isNumeric(stel)) {
			JOptionPane.showConfirmDialog(null, "电话号码不能为非数字!", "提示", JOptionPane.CLOSED_OPTION);
			return;
		}
		if (stel.length() > 11) {
			JOptionPane.showConfirmDialog(null, "电话号码位数错误！", "提示", JOptionPane.CLOSED_OPTION);
			return;
		}
		Student_Dao sDao = new Student_Dao();//sno,sname, ssex,sage,sdept, sclass, stel, null
		if (sDao.stuInsert(new Student(sno, sname, ssex, sage, sdept, sclass, stel, null)) == 1) {//返回值是0
			JOptionPane.showConfirmDialog(null, "添加成功!", "提示", JOptionPane.CLOSED_OPTION);
			ReplaceActionPerformed(evt);
		} else {
			JOptionPane.showConfirmDialog(null, "学号或电话已被使用!请重新输入", "提示", JOptionPane.CLOSED_OPTION);
		}
		BaseDao.close();
	}
