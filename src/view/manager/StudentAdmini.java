	private void InfoTableQMousePressed(java.awt.event.MouseEvent evt) {
		int row = InfoTableQ.getSelectedRow();
		if (row < count) {//初始化定义为0
			RenewSnoQ.setText((String) InfoTableQ.getValueAt(row, 0));
			RenewSnameQ.setText((String) InfoTableQ.getValueAt(row, 1));
			if (InfoTableQ.getValueAt(row, 2).equals("男")) {
				MButton.setSelected(true);
				WButton.setSelected(false);
			} else {
				WButton.setSelected(false);
				MButton.setSelected(true);
			}
			if ((int) InfoTableQ.getValueAt(row, 3) > 0) {
				RenewSageQ.setText(String.valueOf(InfoTableQ.getValueAt(row, 3)));
			} else {
				RenewSageQ.setText(null);
			}
			RenewSdeptQ.setText((String) InfoTableQ.getValueAt(row, 4));
			RenewSclassQ.setText((String) InfoTableQ.getValueAt(row, 5));
			RenewStelQ.setText((String) InfoTableQ.getValueAt(row, 6));
		}
	}
	
